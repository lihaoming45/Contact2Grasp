def forward_mano2(mano_model, pose, beta, transl):
    """Forward mano pass, MANO params to mesh"""

    verts, joints = mano_model(pose, beta, transl)

    verts_homo = verts / 1000
    joints_homo = joints / 1000

    return verts_homo, joints_homo

def to_tensor(array, dtype=torch.float32):
    if not torch.is_tensor(array):
        array = torch.tensor(array)
    return array.to(dtype)

def add_joints(vertices,joints, joint_ids):
    dev = vertices.device

    joint_ids = to_tensor(list(joint_ids.values()),dtype=torch.long).to(dev)
    extra_joints = torch.index_select(vertices, 1, joint_ids)
    joints = torch.cat([joints, extra_joints], dim=1)

    return joints

def vertices2joints(J_regressor, vertices,tips_ids, returns_tips=True):
    ''' Calculates the 3D joint locations from the vertices

    Parameters
    ----------
    J_regressor : torch.tensor JxV
        The regressor array that is used to calculate the joints from the
        position of the vertices
    vertices : torch.tensor BxVx3
        The tensor of mesh vertices

    Returns
    -------
    torch.tensor BxJx3
        The location of the joints
    '''

    joints = torch.einsum('bik,ji->bjk', [vertices, J_regressor])

    if returns_tips:
        joints = add_joints(vertices, joints,tips_ids)


    return joints


def pose_decompose(mano_params,dc_parts=['index', 'middle','small', 'fourth', 'big']):
    dc_params = {k:v for k,v in mano_params.items() if k in ['transl', 'global_orient']}

    hand_pose = mano_params['hand_pose']
    init_part_id = np.asarray(range(9))
    for idx,part in enumerate(dc_parts):
        part_id =init_part_id+(idx*9)
        dc_params[part] = hand_pose[:, part_id]

    return dc_params




